#slicing

lst=[10,25,30,35,40,45,50,55,60]
print(lst[1])
print(lst[4])
print(lst[1:4]) #print 1 to 3
print(lst[2:7])
print(lst[3:5])
print(lst[2:]) #starting to last
print(lst[:5]) #0 to upper-1
print(lst[:])  #print full list
print(lst[-1])
print(lst[-3])
print(lst[0]+lst[-4])
print(lst[-3:-1])
print(lst[-1:-3])  #logic error returns
print(lst[-3:])
print(lst[:-3])